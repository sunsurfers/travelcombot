def get_month_name_by_number(month_number):
    """
    Получить имя месяца по номеру
    """
    if month_number == 1:
        return 'января'
    elif month_number == 2:
        return 'февраля'
    elif month_number == 3:
        return 'марта'
    elif month_number == 4:
        return 'апреля'
    elif month_number == 5:
        return 'мая'
    elif month_number == 6:
        return 'июня'
    elif month_number == 7:
        return 'июля'
    elif month_number == 8:
        return 'августа'
    elif month_number == 9:
        return 'сентября'
    elif month_number == 10:
        return 'октября'
    elif month_number == 11:
        return 'ноября'
    elif month_number == 12:
        return 'декабря'
    return ''

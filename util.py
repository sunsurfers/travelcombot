def get_month_name_by_number(month_number):
    """
    Получить имя месяца по номеру
    """
    if month_number == 1:
        return 'январь'
    elif month_number == 2:
        return 'февраль'
    elif month_number == 3:
        return 'март'
    elif month_number == 4:
        return 'апрель'
    elif month_number == 5:
        return 'май'
    elif month_number == 6:
        return 'июнь'
    elif month_number == 7:
        return 'июль'
    elif month_number == 8:
        return 'август'
    elif month_number == 9:
        return 'сентябрь'
    elif month_number == 10:
        return 'октябрь'
    elif month_number == 11:
        return 'ноябрь'
    elif month_number == 12:
        return 'декабрь'
    return ''
